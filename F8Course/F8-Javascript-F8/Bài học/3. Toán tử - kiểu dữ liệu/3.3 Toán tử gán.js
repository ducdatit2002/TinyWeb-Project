/** Toán tử gán
Toán tử           Ví dụ             Tương đương
=                 x = y             x = y
+=                x +=y             x = x + y
-=                x -=y             x = x - y
*=                x *=y             x = x * y
/=                x /=y             x = x / y
**=                x **=y             x = x ** y
 */

var a = 1;  //1 được gán cho a 

a = a + 2;  // a+2 được gán lại cho a
a += 2 //cách dùng khác

console.log(a);
